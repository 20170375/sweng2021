def draw_matrix(m):
    array = m.get_array()
    for y in range(m.get_dy()):
        for x in range(m.get_dx()):
            if array[y][x] == 0:
                print("□", end='')
            elif array[y][x] == 1:
                print("■", end='')
            else:
                print("XX", end='')
        print()

def rotate_clockwise(m):
    old_array = m.get_array()
    new_matrix = Matrix(old_array)
    new_array = new_matrix.get_array()
    for y in range(m.get_dy()):
        for x in range(m.get_dx()):
            new_array[y][x] = old_array[m.get_dy()-1-x][y]
    return new_matrix

def rotate_counter_clockwise(m):
    old_array = m.get_array()
    new_matrix = Matrix(old_array)
    new_array = new_matrix.get_array()
    for y in range(m.get_dy()):
        for x in range(m.get_dx()):
            new_array[y][x] = old_array[x][m.get_dy()-1-y]
    return new_matrix

